Jatim Park 1 is a recreational and educational park located in Batu City, East Java, Indonesia. It offers a variety of exciting rides and educational facilities, making it a popular destination for family travelers. Jatim Park 1 has a variety of attractions, including a roller coaster, haunted house, and swimming pool, as well as pavilions showcasing culture and science.
Museum Angkut
Museum Angkut is a transportation museum located in Batu City, near Malang, East Java, Indonesia. The museum displays a collection of vehicles from various eras and countries, making it a unique destination that combines education and entertainment.
Jatim Park 2
Jatim Park 2, also located in Batu City, East Java, Indonesia, is a tourist park that combines educational and recreational elements, focusing primarily on animals and the environment. The park consists of three main sections: Batu Secret Zoo, Animal Museum, and Eco Green Park.
Batu Love Garden
Batu Love Garden (Baloga) is a tourist park located in Batu City, East Java, Indonesia. This park stands out with its various collections of flowers and plants, as well as its natural and romantic atmosphere.
Jatim Park 3d
Jatim Park 3, located in Batu City, East Java, Indonesia, is a prominent tourist destination with a futuristic and educational theme. The park consists of various zones that offer unique and interesting experiences for visitors, especially those interested in the world of prehistory, technology, and modern entertainment.
Mount Bromo
Mount Bromo is one of the most famous volcanoes in Indonesia, located in East Java Province, near Malang. The mountain is part of the Bromo Tengger Semeru National Park and is known for its spectacular natural scenery and interesting geological phenomena.
